    private void jBProcurarActionPerformed(java.awt.event.ActionEvent evt) {                                           
            try {
            if((jTNome.getText().equals(""))) {
                throw new CampoNullExc("Campo em branco, por favor Preencha");
            }
            }catch(CampoNullExc x) {
                    JOptionPane.showMessageDialog(null, ""+x.getMessage());
            }
        try {
            FileInputStream arquivoLeitura = new FileInputStream("/home/ProjetoFinal/db/cadCarro.dat");
            ObjectInputStream objLeitura = new ObjectInputStream(arquivoLeitura);
            try{
                while(true){
                    lista.add((Carro)objLeitura.readObject());
                }
            }catch(IOException e) {
                System.out.println("Fim Arquivo.");
            }
            objLeitura.close();
            arquivoLeitura.close();
        } catch (ClassNotFoundException | IOException ex) {
        }
        int aux=0;
        for(int i=0; i<lista.size();i++) {
            if(lista.get(i).getNome().equals(jTNome.getText())) {
                aux=1;
                TelaEditCarro2 tela = new TelaEditCarro2();
                tela.setVisible(true);
                tela.pegarValores(lista.get(i).getNome(), i);
                setVisible(false);
                break;
            }
        }
        if(aux==0) {
            jLabel3.setText("Carro nÃ£o Encontrado");
        }
        lista.clear();
    }                                          

