    private void jBCadastrarActionPerformed(java.awt.event.ActionEvent evt) {                                            
            try {
                if((jTLogin.getText().equals(""))||(jTSenha.getText().equals(""))) {
                    throw new CampoNullExc("Campo em branco, por favor Preencha.");
                }else {
                    //LER
                    try{
                        FileInputStream arquivoLeitura = new FileInputStream("/home/ProjetoFinal/db/cadFunc.dat");
                        ObjectInputStream objLeitura = new ObjectInputStream(arquivoLeitura);
                            try{
                                while(true){
                                    lista.add((Funcionario)objLeitura.readObject());
                                }
                            }catch(IOException e) {
                                System.out.println("Fim Arquivo.");
                            }
                            objLeitura.close();
                    }catch (ClassNotFoundException | IOException ex) {
                    }

                    //NOVO
                    Funcionario func=new Funcionario(jTLogin.getText(), jTSenha.getText());
                    lista.add(func);
                    FileOutputStream arquivoGrav = null;

                    //GRAVAR
                    try {
                        arquivoGrav = new FileOutputStream("/home/ProjetoFinal/db/cadFunc.dat");
                        ObjectOutputStream objGravar = new ObjectOutputStream(arquivoGrav);
                        for(int i=0; i < lista.size();i++) {
                            objGravar.writeObject(lista.get(i));
                        }
                        objGravar.flush();
                        objGravar.close();
                        arquivoGrav.flush();
                        arquivoGrav.close();
                    } catch (IOException ex) {
                        System.out.println("Erro ao gravar");
                    }
                    setVisible(false);
                    lista.clear();
                }
                
                }catch(CampoNullExc x) {
                        JOptionPane.showMessageDialog(null, ""+x.getMessage());
                }
    }                                           

